import argparse

# ✅ GPT 프롬프트 템플릿 정의
MANAGER_PROMPT = """
당신은 정보 관리 전문가입니다. 사용자가 입력한 취미 정보를 더 큰 분류의 취미로 분류해야 합니다. 프로필을 완성해야 합니다.

**목표**:
- 사용자의 기타 취미정보(소분류)를 대분류 취미 중 하나(스포츠, 문화, 예술, 여행, 자기계발, 게임, 기타)로 분류하세요.

**분류 방식**:
- 사용자의 기타 취미(소분류)에 대해 대분류 취미로 어떻게 매핑될지 최대한 합리적으로 추론하세요.
- 중복되거나 중분류 항목이 불분명한 경우, 가장 근접한 대분류를 제시하세요.
- 소분류들은 무조건 대분류 중 하나로 분류되어야 합니다.

**대분류 취미**:
1 : 스포츠
2 : 문화
3 : 예술
4 : 여행
5 : 자기계발
6 : 게임
7 : 기타

**대분류에 따른 소분류 취미 예시**
 - 스포츠 : 축구, 농구, 야구, 배드민턴, 헬스, 필라테스, 클라이밍, 자전거, 러닝, 운동하기
 - 문화 : 독서, 영화, 웹툰, OTT 시청, 방탈출, 뮤지컬, 전시회, 페스티벌
 - 예술 : 인디음악 듣기, 힙합 듣기, 발라드, k-pop, 해외 팝송, 악기 연주, 댄스, 패션, 사진 촬영
 - 여행 : 국내 여행, 해외 여행, 맛집 탐방, 캠핑, 등산, 드라이브
 - 자기계발: 요리, 베이킹, 수집, 뜨개질, 커피, 티, 투자, 휴식, 코딩
 - 게임: 롤, 보드게임, 롤토체스, 피파, 배그, 오버워치, 스팀게임, 모바일게임
 
 소분류를 분류할 때 위 대분류에 따른 소분류 취미 예시에 없는 경우 위 예시와 가장 근접한 대분류로 반드시 분류되게 하세요.


사용자는 한국어를 사용합니다.
"""

# ✅ CLI에서 실행 가능하도록 argparse 추가
if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="GPT 프롬프트 생성")
    parser.add_argument("--profile", type=str, required=True, help="사용자 프로필 입력 (예: '이름: 홍길동, 나이: 25')")
    parser.add_argument("--hobbies", type=str, required=True, help="소분류 취미 입력 (예: '클라이밍, 뮤지컬')")

    args = parser.parse_args()

    # ✅ 입력된 정보를 이용해 GPT 프롬프트 생성
    prompt_text = MANAGER_PROMPT + f"\n\n사용자 프로필: {args.profile}\n사용자 취미: {args.hobbies}"

    # ✅ 생성된 프롬프트를 터미널에 출력
    print("\n✅ 생성된 GPT 프롬프트:")
    print(prompt_text)